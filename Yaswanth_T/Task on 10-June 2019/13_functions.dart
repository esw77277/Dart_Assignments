main()
{
 add(10,2);
 mul(2,5);
 sub(20,10);
 div(30,10);
}

add(int a,int b)
{
int q=a+b;
 print("addition of $a and $b :$q");
}

mul(int q,int w)
{
int e = q*w;
print("multiplication of $q and $w:$e");
}

sub(int z,int v)
{
int x = z-v;
print("subraction of $z and $v:$x");
}

div(int h,int t)
{
int j = h/t;
print("Division of $h and $t : $j);
}